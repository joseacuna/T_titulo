    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
               Connection miConexion=(Connection) Conexion.GetConnection();
        try
        {
                    DefaultTableModel modelo =  (DefaultTableModel) jTable1.getModel();
        
        String criterio = txtBusqueda.getText();
        
             Statement statement=(Statement) miConexion.createStatement();
             ResultSet rs;

             if(rbtnCliente.isSelected())
             {
                 rs = statement.executeQuery("SELECT * FROM tb_reserva where rut LIKE '%"+ criterio +"%'");       
             }
             else
             {
                 rs = statement.executeQuery("SELECT * FROM tb_reserva where num ='"+ criterio +"'");    
                 
             }
             LimpiarGrilla();
             while(rs.next())
             {

                 modelo.addRow(new Object[]{ rs.getInt("num"), rs.getString("fecha"), rs.getString("fecha_entrega"), rs.getString("rut") } );          
             }
             rs.close();
             statement.close();
             miConexion.close();     
        }
        catch (Exception ex)
        {
            JOptionPane.showMessageDialog(this, "Error "+ex.getMessage());
        }     
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void LimpiarGrilla()
    {
        DefaultTableModel modelo =  (DefaultTableModel) jTable1.getModel();

       for (int i = 0; i < jTable1.getRowCount(); i++) {
           modelo.removeRow(i);
           i-=1;
       }        
    }
    
    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        if (this.jTable1.getRowCount()==0) {
            JOptionPane.showMessageDialog (null, "No hay datos en la tabla para exportar.","BCO",
                JOptionPane.INFORMATION_MESSAGE);

            return;
        }
        JFileChooser chooser=new JFileChooser();
        FileNameExtensionFilter filter=new FileNameExtensionFilter("Archivos de excel","xls");
        chooser.setFileFilter(filter);
        chooser.setDialogTitle("Guardar archivo");
        chooser.setMultiSelectionEnabled(false);
        chooser.setAcceptAllFileFilterUsed(false);
        if (chooser.showSaveDialog(null)==JFileChooser.APPROVE_OPTION){
            List<JTable> tb=new ArrayList<>();
            List<String>nom=new ArrayList<>();
            tb.add(jTable1);
            nom.add("Detalle de Gastos");
            String file=chooser.getSelectedFile().toString().concat(".xls");
            try {
                Exporter e=new Exporter(new File(file),tb, nom);
                if (e.export()) {
                    JOptionPane.showMessageDialog(null, "Los datos fueron exportados a excel.","BCO",
                        JOptionPane.INFORMATION_MESSAGE);

                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,"Hubo un error"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton8ActionPerformed

