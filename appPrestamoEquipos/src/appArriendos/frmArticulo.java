    private void btnEliminarEquipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarEquipoActionPerformed
        // TODO add your handling code here:
        try
        {
            if(txtcodigo.getText().trim().isEmpty()){
                JOptionPane.showMessageDialog(this, "Debe seleccionar Registro Administrador a eliminar");
            }
            else
            {
                
                Connection miConexion=(Connection) Conexion.GetConnection();
                Statement statement=(Statement) miConexion.createStatement();
                String codigo = txtcodigo.getText();
                ResultSet rs = statement.executeQuery("SELECT * FROM tb_articulo where codigo = '"+ codigo +"'");
                int estaDisponible = 0;
                while (rs.next()) {
                    estaDisponible= rs.getInt("disponible");
                }
                if(estaDisponible == 2){
                    statement.execute("INSERT INTO tb_articuloeliminado SELECT * FROM tb_articulo where codigo = '"+ codigo +"'");
                    int cantidad = statement.executeUpdate("DELETE FROM tb_articulo where codigo = '"+ codigo +"'");
                    if(cantidad > 0)
                    {
                        JOptionPane.showMessageDialog(this, "Registro eliminado exitosamente");
                        Limpiar();
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this, "Problema eliminando registro de sistema");
                    }
                }else{
                        JOptionPane.showMessageDialog(this, "El articulo no ha sido dado de baja");
                    }}
        } catch (Exception ex)
        {
            JOptionPane.showMessageDialog(this, "Error "+ex.getMessage());
        }
    }//GEN-LAST:event_btnEliminarEquipoActionPerformed

