    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Connection miConexion=(Connection) Conexion.GetConnection();
        try
        {
             if(txtrut.getText().trim().isEmpty())
            {
               JOptionPane.showMessageDialog(this, "Debe ingresar campo rut"); 
            }
             else if(txtnombre.getText().trim().isEmpty())
            {
               JOptionPane.showMessageDialog(this, "Debe ingresar campo nombre"); 
            }
             else if(txtusuario.getText().trim().isEmpty())
            {
               JOptionPane.showMessageDialog(this, "Debe ingresar campo nombre de usuario"); 
            }
             else if(txtcontrasena.getText().trim().isEmpty())
            {
               JOptionPane.showMessageDialog(this, "Debe ingresar campo contraseña de usuario"); 
            }             
            else
            {
                  Statement statement=(Statement) miConexion.createStatement();

                  String rut = txtrut.getText();
                  String nombre = txtnombre.getText();
                  String telefono = txttelefono.getText();
                  String celular = txtcelular.getText();
                  String email = txtemail.getText();
                  String direccion = txtdireccion.getText();
                  String usuario = txtusuario.getText();
                  String contrasena = txtcontrasena.getText();
                  
                  int tipo = 1;
                  String tipo_usuario = (String) cboxTipoUsuario.getSelectedItem();
                  if(tipo_usuario == "ADMINISTRATIVO")
                  {
                      tipo = 2;
                  }
                  if(tipo_usuario == "INSTALADOR")
                  {
                     tipo = 3; 
                  }
                   ResultSet rs = statement.executeQuery("SELECT COUNT(*) AS num FROM tb_administrador WHERE rut ='"+ rut +"' ");
                   int rows= 0;
                   if(rs.next()){
                     rows = rs.getInt("num");
                   }
                  if(rows > 0){
                       JOptionPane.showMessageDialog(this, "Registro ingresado existente en sistema");                      
                  }
                  else
                  {
                       statement.execute("insert into tb_administrador values('"+rut+"','"+nombre+"','"+telefono+"','"+celular+"','"+email+"','"+direccion+"','"+usuario+"','"+contrasena+"','"+tipo+"')");

                       JOptionPane.showMessageDialog(this, "Datos ingresados correctamente");

                       Limpiar();
                       statement.close();
                       miConexion.close();                       
                  }
              
            }
        }
        catch (Exception ex)
        {
            JOptionPane.showMessageDialog(this, "Error "+ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

