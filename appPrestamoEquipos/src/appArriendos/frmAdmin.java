   public static boolean ValidarRut(int rut, char dv)
    {
        int m = 0, s = 1;
        for (; rut != 0; rut /= 10)
        {
            s = (s + rut % 10 * (9 - m++ % 6)) % 11;
        }
        return dv == (char) (s != 0 ? s + 47 : 75);
    }
   
      private void ObtenerRegistro(String rut){
        
        try
        {
             ResultSet rs = null;
             Connection miConexion=(Connection) Conexion.GetConnection();
             Statement statement=(Statement) miConexion.createStatement();
             rs = statement.executeQuery("SELECT * from tb_administrador where rut = '"+ rut +"'");
             
             while(rs.next()){
                 
                 txtrut.setText(rs.getString("rut"));
                 txtnombre.setText(rs.getString("nombre"));
                 txttelefono.setText(rs.getString("telefono"));
                 txtcelular.setText(rs.getString("celular"));
                 txtemail.setText(rs.getString("email"));
                 txtdireccion.setText(rs.getString("direccion"));
                 txtusuario.setText(rs.getString("usuario"));
                 txtcontrasena.setText(rs.getString("contrasena"));
                 
                int tipo_usuario = rs.getInt("tipo_usuario");
                
                if(tipo_usuario == 1){
                    this.cboxTipoUsuario.setSelectedItem("ADMINISTRADOR") ;
                }
                if(tipo_usuario == 2){
                    this.cboxTipoUsuario.setSelectedItem("ADMINISTRATIVO") ;
                }
                if(tipo_usuario == 3){
                    this.cboxTipoUsuario.setSelectedItem("INSTALADOR") ;
                }
             }
        }
        catch (Exception ex)
        {
            JOptionPane.showMessageDialog(this, "Error "+ex.getMessage());
        }

    }
      
